#Faça um programa que receba duas notas digitadas pelo usuário. Se a nota for maior ou igual a seis, escreva aprovado, senão escreva reprovado.   
nota1 = input("Digite a nota do aluno 1")
nota2 = input("Digite a nota do aluno 2")

if nota1 >='6':
    print("Aluno 1 aprovado")
else:
    print("Aluno 1 reprovado")

if nota2 >='6':
    print("Aluno 2 aprovado")
else:
    print("Aluno 2 reprovado")